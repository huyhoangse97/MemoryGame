package com.example.memorygame;

public class AnimationFlag {
    int flag;

    public AnimationFlag(){
        this.flag = 1;
    }

    public AnimationFlag(int flag){
        this.flag = flag;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }
}
